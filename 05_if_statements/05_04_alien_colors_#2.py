# Runs "if"
alien_color = 'green'

if alien_color == 'green':
    print("You shot down a green alien! You got 5 points!")
else:
    print(f"You shot down a {alien_color} alien! You got 10 points!")

# Runs "else"
alien_color = 'yellow'

if alien_color == 'green':
    print("You shot down a green alien! You got 5 points!")
else:
    print(f"You shot down a {alien_color} alien! You got 10 points!")