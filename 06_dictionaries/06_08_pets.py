# Creating dictionaries
pet_1 = {
    'animal type': 'cat',
    'owner': 'Soren'
    }

pet_2 = {
    'animal type': 'dog',
    'owner': 'Haley'
    }

pet_3 = {
    'animal type': 'bearded dragon',
    'owner': 'Cain'
    }

# Storing dictionaries in list
pets = [pet_1, pet_2, pet_3]

# Printing information
for pet in pets:
    print(pet)
